print(phrase.upper())  # GIRAFFE ACADEMY
print(phrase.islower())  # False
print(phrase.isupper())  # False
print(len(phrase))  # 15
print(phrase[0])  # G
print(phrase.index("Acad"))  # 8
print(phrase.replace("Giraffe", "Elephant"))  # Elephant Academy

